package com.spring.capstone.DTO;

public class cfnumbersDTO {
    private int cfId;
    private String cfNumber;
    private String cfName;
    private String cfArea;

    public int getCfId() {
        return cfId;
    }

    public void setCfId(int cfId) {
        this.cfId = cfId;
    }

    public String getCfNumber() {
        return cfNumber;
    }

    public void setCfNumber(String cfNumber) {
        this.cfNumber = cfNumber;
    }

    public String getCfName() {
        return cfName;
    }

    public void setCfName(String cfName) {
        this.cfName = cfName;
    }

    public String getCfArea() {
        return cfArea;
    }

    public void setCfArea(String cfArea) {
        this.cfArea = cfArea;
    }
}
